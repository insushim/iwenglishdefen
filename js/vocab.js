function getQuizQuestion() {
  const correct = VOCAB_DATA[Math.floor(Math.random() * VOCAB_DATA.length)];
  const isEnToKo = Math.random() > 0.5;
  const question = isEnToKo ? correct.en : correct.ko;
  const answer = isEnToKo ? correct.ko : correct.en;
  const choices = [answer];
  const usedIdx = new Set([VOCAB_DATA.indexOf(correct)]);
  while (choices.length < 4) {
    const idx = Math.floor(Math.random() * VOCAB_DATA.length);
    if (usedIdx.has(idx)) continue;
    usedIdx.add(idx);
    choices.push(isEnToKo ? VOCAB_DATA[idx].ko : VOCAB_DATA[idx].en);
  }
  // Shuffle
  for (let i = choices.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [choices[i], choices[j]] = [choices[j], choices[i]];
  }
  return {
    question,
    answer,
    choices,
    isEnToKo,
    hint: isEnToKo ? "🇺🇸 → 🇰🇷" : "🇰🇷 → 🇺🇸",
  };
}
